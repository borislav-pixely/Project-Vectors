int menu(char* menu, char* input) {
	unsigned short userInput = 0;
	if (input == "cin") {
		show(menu);
		cin >> userInput;
	} else if (input == "file") {
		ifstream file("input.txt");
		if (!file) {
			error("Файлът \"input.txt\" не съществува, моля създайте го");
			return -1;
		} else {
			show(menu);
			file >> userInput;
		}
	}
	cout << endl;
	return userInput;
}

